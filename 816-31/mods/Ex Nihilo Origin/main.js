function randomNum(minNum, maxNum, q) {
	var i;
	var n;
	var m = maxNum - minNum + 1,
	a = function(k) {
		return ((q - 1) * (k - 1) / (m - 1) + 1) * 2 / (m * (q + 1))
	},
	s = function(k) {
		return (a(1) + a(k)) * k / 2
	};
	for (i = minNum; i <= maxNum; i += 1) {
		n = i - minNum + 1;
		if (Math.random() >= s(n - 1) && Math.random() < s(n)) {
			return i
		}
	}
	return maxNum ? maxNum: 0
}
