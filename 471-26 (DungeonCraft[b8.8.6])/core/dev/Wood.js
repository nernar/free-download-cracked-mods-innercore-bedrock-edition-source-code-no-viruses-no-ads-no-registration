Callback.addCallback("ItemUse", function (coords, item, block, isExter, player) {
    let bs = BlockSource.getDefaultForActor(player);
    if (item.id == 351 && item.data == 15) {
        if (bs.getBlock(coords.x, coords.y, coords.z).id == BlockID.sap) {
            setWood(coords.x, coords.y - 1, coords.z);
        }
    }
});
TileEntity.registerPrototype(BlockID.sap, {defaultValues: {}, tick: function () {
    if (Math.random() * 1000 < 1) {
        setWood(this.x, this.y - 1, this.z, this.blockSource);
    }
}});

